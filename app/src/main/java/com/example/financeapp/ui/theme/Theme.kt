@Composable
fun FinanceAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window

            WindowCompat.setDecorFitsSystemWindows(window, false)
            val insetsController = WindowCompat.getInsetsController(window, view)

            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.VANILLA_ICE_CREAM) {
                window.decorView.setOnApplyWindowInsetsListener { v, insets ->
                    v.setBackgroundColor(colorScheme.primary.toArgb())
                    insets
                }
            } else {
                window.statusBarColor = colorScheme.primary.toArgb()
            }

            window.navigationBarColor = colorScheme.primary.toArgb()

            val isStatusBarLight = isColorLight(colorScheme.primary)
            val isNavBarLight = isColorLight(colorScheme.primary)

            insetsController.isAppearanceLightStatusBars = isStatusBarLight
            insetsController.isAppearanceLightNavigationBars = isNavBarLight

        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}

fun isColorLight(color: androidx.compose.ui.graphics.Color): Boolean {
    return ColorUtils.calculateLuminance(color.toArgb()) > 0.5
}
